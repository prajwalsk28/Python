# WAP to calculate area of rectangle
side1 = int(input("Enter 1st side: "))
side2 = int(input("Enter 2nd side: "))
area = 2*(side1 + side2)
print("area of rectangle is: ",area)

# WAP to calculate the average of two floating values
val1 = float(input("Enter 1st value: "))
val2 = float(input("Enter 2nd value: "))
average = (val1 + val2)/2
print("average of given values is: ",average)

